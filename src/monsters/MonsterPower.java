/**
 * Enumeration of monster special powers.
 */
public enum MonsterPower {
    /** Uses wings to thrust powerful winds at target. */
    WIND_THRUST,
    /** Uses poisonous bite to inject venom into target  */
    POISON_BITE,
    /** Shoots sticky webs to trap enemies. */
    WEB_TRAP,
    /** Splashes corrosive slime at enemies. */
    SLIME_SPLASH,
    /** Teleports to dodge or attack. */
    SHADOW_STEP,
    /** Spits acid to burn targets. */
    ACID_SPIT,
    /** Allows the monster to fly. */
    FLIGHT,
    /** Smashes the ground to cause a shockwave. */
    EARTH_SMASH
}